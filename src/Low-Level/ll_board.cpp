//--------------------------------------------------------------------------------------------------
// FONCTION    : _InitClocks
//
// DESCRIPTION : Initialisation des clocks du micro
//--------------------------------------------------------------------------------------------------
static void _InitClocks(void)
{
}

//--------------------------------------------------------------------------------------------------
// FONCTION    : _InitGpio
//
// DESCRIPTION : Initiliasation des GPIO
//--------------------------------------------------------------------------------------------------
static void _InitGpio(void)
{
    pinMode(GPIO_PIN_BUILTIN_LED, OUTPUT);
}

static void _InitSerial(void)
{
    Serial.begin(115200);
}

static void _InitUsb(void)
{
}

static void _InitCan(void)
{
}

//------------------------------------------------------------------------------------------------//
//---                                        Partagees                                         ---//
//------------------------------------------------------------------------------------------------//

//--------------------------------------------------------------------------------------------------
// FONCTION    : BOARD_Init
//
// DESCRIPTION : Initialisation de la carte : GPIO, Clocks, Interruptions...
//--------------------------------------------------------------------------------------------------
void BOARD_Init(void)
{
    //--- Initialisation des clocks
    _InitClocks();

    //--- Initialisation des GPIO
    _InitGpio();

    _InitSerial();

    _InitUsb();

    _InitCan();
}

uint32_t BOARD_GetTick(void)
{
    return millis();
}

void BOARD_Delayms(uint32_t value)
{
    delay(value);
}

void BOARD_Delayus(uint32_t value)
{
    delayMicroseconds(value);
}